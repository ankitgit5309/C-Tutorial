#include<stdio.h>

int main(int argc, char const *argv[])
{
    int i=0,n=9,factorial=1;
    for(i=1;i=n;i++)
    {
       factorial *=1;
    }
    printf("Factorial of the %d is %d",n,factorial);
    return 0;
}